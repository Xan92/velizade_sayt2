const LocaleContext = createContext();

export const useLocale = () => useContext(LocaleContext);

export const LocaleProvider = ({ children }) => {
  const [lang, setLang] = useState(() => {
    return localStorage.getItem('site_lang') || 'az';
  });

  useEffect(() => {
    localStorage.setItem('site_lang', lang);
  }, [lang]);

  const t = (key) => {
    return translations[lang]?.[key] || translations['az'][key] || key;
  };

  // Helper to extract localized string from DB object or fallback string
  const getLoc = (dbField) => {
    if (!dbField) return '';
    if (typeof dbField === 'string') return dbField; // old data backwards compatibility
    return dbField[lang] || dbField['az'] || '';
  };

  return (
    <LocaleContext.Provider value={{ lang, setLang, t, getLoc }}>
      {children}
    </LocaleContext.Provider>
  );
};
